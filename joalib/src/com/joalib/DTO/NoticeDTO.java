package com.joalib.DTO;

public class NoticeDTO {
	
	int notice_no;
	String notice_title;
	String notice_text;
	String notice_date;
	String notice_attach;
	
	
	public int getNotice_no() {
		return notice_no;
	}
	public void setNotice_no(int notice_no) {
		this.notice_no = notice_no;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public String getNotice_text() {
		return notice_text;
	}
	public void setNotice_text(String notice_text) {
		this.notice_text = notice_text;
	}
	public String getNotice_date() {
		return notice_date;
	}
	public void setNotice_date(String notice_date) {
		this.notice_date = notice_date;
	}
	public String getNotice_attach() {
		return notice_attach;
	}
	public void setNotice_attach(String notice_attach) {
		this.notice_attach = notice_attach;
	}

}
